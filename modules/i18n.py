def tr(lang: str, key: str, **kwargs: str | int) -> str:
    """Translate `key` for `lang` (ja|en). `kwargs` for str.format."""
    table = _STR.get(lang) or _STR[LANG_JA]
    template = table.get(key) or _STR[LANG_JA].get(key) or key
    if kwargs:
        return template.format(**kwargs)
    return template


def is_english(lang: str) -> bool:
    return lang == LANG_EN
